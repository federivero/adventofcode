let order = ['N', 'S', 'W', 'E'];

let round = 1;
for (round = 1; ; round++){
    let proposals = {};
    
    for (let e = 0; e < elves.length; e++){
        let elf = elves[e];
        const nw = isEmpty(elf.r - 1, elf.c - 1)
        const n = isEmpty(elf.r - 1, elf.c)
        const ne = isEmpty(elf.r - 1, elf.c + 1)
        const w = isEmpty(elf.r, elf.c - 1)
        const east = isEmpty(elf.r, elf.c + 1)
        const sw = isEmpty(elf.r + 1, elf.c - 1)
        const s = isEmpty(elf.r + 1, elf.c)
        const se = isEmpty(elf.r + 1, elf.c + 1)   
        elf.move = 0;
        if ( nw && n && ne && east && w && se && s && sw){
            continue; // elf wont move
        }
        // propose movements
        for (let i = 0; i < order.length && !elf.move; i++){
            switch (order[i]){
                case "N":
                    if (nw && n && ne){
                        elf.move = 1;
                        elf.dir = order[i];
                    }
                    break;
                case "E":
                    if (ne && east && se){
                        elf.move = 1;
                        elf.dir = order[i];
                    }
                    break;
                case "W":
                    if (nw && w && sw){
                        elf.move = 1;
                        elf.dir = order[i];
                    }
                    break;
                case "S":
                    if (sw && s && se){
                        elf.move = 1;
                        elf.dir = order[i];
                    }
                    break;
            }
        }
        if (elf.move){
            const key = getKey(elf)
            if (!proposals[key])
                proposals[key] = 1
            else
                proposals[key]++;
        }
    }

    // phase 2: move
    let elfMoved = false;
    for (let e = 0; e < elves.length; e++){
        let elf = elves[e];
        let key = getKey(elf);
        if (elf.move){
            elfMoved = true;
            if (proposals[key] === 1){
                switch (elf.dir){
                    case 'N':
                        elf.r--;
                        break;
                    case 'S':
                        elf.r++;
                        break;
                    case 'W':
                        elf.c--;
                        break;
                    case 'E':
                        elf.c++;
                        break;                
                }
            }    
        }
    }

    let rowbefore = false;
    let rowafter = false;
    let columnbefore = false;
    let columnafter = false;
    for (let e = 0; e < elves.length; e++){
        let elf = elves[e];
        if (elf.r < 0){
            rowbefore = true;
        }
        if (elf.c < 0){
            columnbefore = true;
        }
        if (elf.r >= h){
            rowafter = true;
        }
        if (elf.c >= w){
            columnafter = true;
        }   
    }

    if (rowbefore){
        h++;
        for (let e = 0; e < elves.length; e++){
            elves[e].r++;
        }
    }
    if (rowafter){
        h++;
    }
    if (columnbefore){
        w++;
        for (let e = 0; e < elves.length; e++){
            elves[e].c++;
        }
    }
    if (columnafter){
        w++;
    }
    
    // generate map again
    generateMap();

    // change proposal order
    order.push(order.shift());   
    
    if (!elfMoved){
        break;
    }

    if (round % 1000 === 0)
        console.log(round);
}

