let lines = input.split("\n")

let maps = [];
let map = { lines: [], index: 0 }
for (let i = 0; i < lines.length; i++){
    let line = lines[i];
    if (line === ''){
        maps.push(map);
        map = { lines: [], index: maps.length };
    }else{
        map.lines.push(line);
    }
}

maps.push(map);

function sameRows(r1, r2){
    return r1 == r2;
}

function sameColumns(map, c1, c2){
    let lines = map.lines;
    for (let i = 0; i < lines.length; i++){
        if (lines[i][c1] !== lines[i][c2]){
            return false
        }
    }
    return true;
}

maps.map((map) => {
    let h = map.lines.length;
    let w = map.lines[0].length;

    // test vertical
    for (let r = 0; r < h - 1; r++){
        let length = Math.min(r + 1, h - 1 - r);
        let isMirror = true;
        for (let i = 1; i <= length; i++){
            let h1 = (r + 1) - i;
            let h2 = (r) + i;
            if (!sameRows(map.lines[h1], map.lines[h2])){
                isMirror = false;
                break;
            }
        }
        if (isMirror){
            map.mirrorY = r;
            break;
        }
    }

    if (map.mirrorY === undefined){
        // test horizontal
        for (let c = 0; c < w - 1; c++){
            let length = Math.min(c + 1, w - 1 - c);
            let isMirror = true;
            for (let i = 1; i <= length; i++){
                let c1 = (c + 1) - i;
                let c2 = (c) + i;
                if (!sameColumns(map, c1, c2)){
                    isMirror = false;
                    break;
                }
            }
            if (isMirror){
                map.mirrorX = c;
                break;
            }
        }
    }
})

let total = 0;
for (let i = 0; i < maps.length; i++){
    if (maps[i].mirrorY !== undefined){
        total += (maps[i].mirrorY + 1)  * 100;
    }else if (maps[i].mirrorX !== undefined){
        total += maps[i].mirrorX + 1;
    }else{
        console.log(" MAP WITHOUT MIRROR");
    }
}

