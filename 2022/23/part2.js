let map = input.split("\n");
let h = map.length;
let w = map[0].length;
let elves = [];

for (let i = 0; i < map.length; i++){
    let line = map[i];
    for (let j = 0; j < line.length; j++){
        if (line[j] === "#"){
            elves.push({ r: i, c: j });
        }
    }
}

function generateMap(){
    map = [];
    for (let i = 0; i < h; i++){
        map.push([]);
        for (let j = 0; j < w; j++){
            map[i][j] = ".";
        }
    }
    
    // place elves
    for (let e = 0; e < elves.length; e++){
        let elf = elves[e];
        map[elf.r][elf.c] = "#";
    }
}

generateMap()

function isEmpty(r, c){
    if (r < 0 || c < 0 || r >= h){
        return true;
    }
    if (c >= w){
        return true;
    }
    return map[r][c] === ".";
}

function getKey(elf){
    switch(elf.dir){
        case 'N':
            return `r${(elf.r - 1)}-c${elf.c}`
        case 'S':
            return `r${(elf.r + 1)}-c${elf.c}`
        case 'E':
            return `r${(elf.r)}-c${elf.c + 1}`
        case 'W':
            return `r${(elf.r)}-c${elf.c - 1}`
    }
}

